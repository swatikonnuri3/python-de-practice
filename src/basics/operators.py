a = 10
b = 3

# Arithmetic operators
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)

# Comparison operators
print("a > b:", a > b)
print("a == b:", a == b)

# Logical operators
print("AND:", a > 5 and b < 5)
print("OR:", a > 5 or b > 5)
